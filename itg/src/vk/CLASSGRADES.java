package vk;

public class CLASSGRADES {

	public static void main(String[] args) {
		int marks=50;
		if(marks>60) {
			System.out.println("First Class");
		}
		else if(marks>55) {
			System.out.println("Second Class");
		}
		else if(marks>=50) {
			System.out.println("Third Class");
		}
		else {
			System.out.println("Fail");
		}
	}

}